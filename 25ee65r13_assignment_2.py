#We are gonna implement the Deep Fool attack to our model
#As we are gonna increse the number of iterations and over shoot the attack strength increases
def deep_fool_attack(image,model,num_classes=len(classes),iterations=20,over_shoot=0.03):
  model.eval()
  image = image.clone().detach().to(device)
  image.requires_grad = True

  output = model(image)
  i,label = torch.max(output.data,1)

  perturbations = torch.zeros_like(image).to(device)

  iteration = 0

  while iteration < iterations:

    output = model(image)
    current_label = torch.argmax(output)


    if current_label != label:
      break

    model.zero_grad()
    output[0,current_label].backward(retain_graph=True)
    grad_orig = image.grad.data.clone()

    min_pert = float('inf')

    for k in range(num_classes):

      if k == current_label:
        continue

      image.grad.zero_()

      output[0,k].backward(retain_graph=True)
      grad_k = image.grad.data.clone()

      w_k = grad_k - grad_orig
      f_k = (output[0,k] - output[0, current_label]).data

      pert_k = torch.abs(f_k) / torch.norm(w_k.flatten())

      if pert_k < min_pert:
        min_pert = pert_k
        w = w_k
    r_i = (min_pert * w) / torch.norm(w.flatten())

    perturbations+= r_i

    image = image + (1 + over_shoot) * r_i
    image = image.detach()
    image.requires_grad = True

    iteration += 1

  return image

