#We are defining the Residual block for the CNN
class ResBlock(nn.Module):

  def __init__(self,input_channels,output_channels):
    super(ResBlock,self).__init__()

    self.conv1 = nn.Conv2d(input_channels,output_channels,kernel_size=3,padding=1)
    self.batchnormlayer1 = nn.BatchNorm2d(output_channels)

    self.conv2 = nn.Conv2d(output_channels,output_channels,kernel_size=3,padding=1)
    self.batchnormlayer2 = nn.BatchNorm2d(output_channels)

    #We are using this to tackle the case where no. of inputs are not equal to the no. of outputs
    if input_channels != output_channels:
      self.conv3 = nn.Conv2d(input_channels,output_channels,kernel_size=1,padding=0)
    else:
      self.conv3 = None

  def forward(self,x):
    residual = self.conv3(x)

    output = F.relu(self.batchnormlayer1(self.conv1(x)))

    output = self.batchnormlayer2(self.conv2(output))

    output = F.relu(output + residual)

    return output

#This is our Convolutional Neural Network
class CNNetwork(nn.Module):

  def __init__(self,num_of_classes = len(classes)):

    super().__init__()

    self.conv1 = nn.Conv2d(3,32,3,padding=1)
    self.bn1 = nn.BatchNorm2d(32)

    #Inclusion of the Residual blocks that we have defined earlier
    self.res1 = ResBlock(32,64)
    self.res2 = ResBlock(64,128)

    self.pool = nn.MaxPool2d(2)

    self.gap = nn.AdaptiveAvgPool2d(1)

    self.classifier = nn.Conv2d(128,num_of_classes,1)

  def forward(self,x):
    x = F.relu(self.bn1(self.conv1(x)))
    x = self.pool(x)

    x = self.res1(x)
    x = self.pool(x)

    x = self.res2(x)
    x= self.pool(x)

    x= self.gap(x)

    x = self.classifier(x)

    x = x.view(x.size(0),-1)

    return x

